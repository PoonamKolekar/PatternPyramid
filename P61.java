package com.jsp.pattern;

public class P61 {
	public static void main(String[] args) {
		
	
		
		int n=7;
		int sp=n/2;
		int st =1;
		for(int i=1;i<=n;i++) {
			for( int s=1;s<=sp;sp++) {
				System.out.print(" ");
			}
			int k=1;
		for(int j=1;j<=st;j++) {
			System.out.print(k);
			

		if(j<=st/2) {
			k++;
		}else {
			k--;
		}}
		System.out.println();

		}}
		}
